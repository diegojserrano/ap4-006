// En java un conjunto de instrucciones envueltas en llaves se denomina "bloque"
public class Programa {

    // El punto de entrada es el bloque desde donde se inicia la ejecución
    // En java el punto de entrada es el bloque (método) main

    // IntelliJ tiene "abreviaturas"
    // para el bloque main escribimos main<Tab>
    public static void main(String[] args) {

        // Para imprimir: System.out.println();
        // La abreviatura es sout<Tab>
        System.out.println("Hola Mundo!!");
        System.out.println("Hoy miercoles 22, estoy en la clase 1");


        // Quiero sumar dos números
        // Para crear una variable hay que declararla
        // tipo identificador = valor inicial;

        int numero1 = 34;
        int numero2 = 78;

        // La operación de asignación espera a que la suma se termine de calcular
        // y guarda el resultado
        int suma = numero1 + numero2;

        // Imprimir el resultado que está guardado en la variable suma
        System.out.println("La suma es: " + suma);

        // Agregar los resultados de diferencia, producto y cociente de los mismos números.

    }



}
